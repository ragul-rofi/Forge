export function getQuestionsForMode(mode, validateTarget) {
  if (mode === 'general') return GENERAL_QUESTIONS;
  if (mode === 'advanced') return [...GENERAL_QUESTIONS, ...ADVANCED_QUESTIONS];
  if (mode === 'validate') {
    const domainQIds = VALIDATE_DOMAIN_QUESTIONS[validateTarget] || [];
    const domainQs = domainQIds.map(id => GENERAL_QUESTIONS.find(q => q.id === id)).filter(Boolean);
    return [...VALIDATE_QUESTIONS, ...domainQs];
  }
  return GENERAL_QUESTIONS;
}
